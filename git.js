/* git basic commands

- git clone : Git clone is a command for downloading existing source code from a remote repository
- git add : Moves changes from the working directory to the staging area
- git commit : The git commit command records the changes made to the repository.We can add a message to describe the changes you've made
- git push : This command sends the committed changes of master branch to your remote repository
- git pull : The git pull command is used to get updates from the remote repo.
- git checkout: It allows you to switch between branches, restore files, and even travel back in time by checking out a specific commit.
- git status: The Git status command gives us all the necessary information about the current branch.
- git branch: The git branch command lists all the branches in your repository. You can create new branches, delete branches, and switch between branches
- git log: The git log command shows a history of commits made to the repository.







*/